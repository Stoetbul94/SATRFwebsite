import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const issfDir = path.join(root, 'public', 'documents', 'issf');
const BASE = 'https://www.issf-sports.org';

const SUBPAGES = [
  { path: '/rules', category: 'rulebook', section: 'ISSF Rule Book 2026' },
  { path: '/rules/code-of-ethics', category: 'integrity', section: 'Integrity & Ethics' },
  { path: '/rules/safeguarding-regulations', category: 'safeguarding', section: 'Safeguarding' },
  { path: '/rules/disciplinary-regulations', category: 'disciplinary', section: 'Disciplinary Regulations' },
  { path: '/rules/eligibility', category: 'eligibility', section: 'Eligibility & Commercial Rights' },
  { path: '/rules/forms', category: 'forms', section: 'Forms' },
  { path: '/rules/amendments-and-interpretation', category: 'guides', section: 'Amendments & Interpretation' },
  { path: '/rules/general-technical-rules', category: 'technical', section: 'General Technical Rules' },
  { path: '/rules/anti-doping', category: 'doping', section: 'Anti-Doping' },
  { path: '/rules/pistol', category: 'guides', section: 'Pistol Rules & Guides' },
  { path: '/rules/shotgun', category: 'guides', section: 'Shotgun Rules & Guides' },
];

function decode(s) {
  return s.replace(/&amp;/g, '&');
}

function slugify(name) {
  return name
    .replace(/\.[a-z0-9]+$/i, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 80);
}

function extractPdfLinks(html) {
  const links = [];
  const re = /<a[^>]+href="([^"]+)"[^>]*>([^<]+)<\/a>/gi;
  let m;
  while ((m = re.exec(html))) {
    const href = decode(m[1]);
    const title = m[2].trim();
    if (/getfile\.aspx/i.test(href)) links.push({ title, pdfUrl: href });
  }
  return links;
}

function findLocalFile(title, files) {
  const slug = slugify(title);
  return (
    files.find((f) => f.replace(/\.pdf$/i, '') === slug) ||
    files.find((f) => f.replace(/\.pdf$/i, '').startsWith(slug.slice(0, 50))) ||
    files.find((f) => slug.startsWith(f.replace(/\.pdf$/i, '').slice(0, 50)))
  );
}

async function fetchHtml(url) {
  const res = await fetch(url, { headers: { 'User-Agent': 'SATRF-Rules-Sync/1.0' } });
  if (!res.ok) return null;
  return res.text();
}

async function main() {
  const files = fs.readdirSync(issfDir);
  const docs = [];
  const seen = new Set();

  for (const page of SUBPAGES) {
    const webUrl = `${BASE}${page.path}`;
    const html = await fetchHtml(webUrl);
    if (!html) {
      console.warn(`Skip ${webUrl} (not found)`);
      continue;
    }
    for (const { title, pdfUrl } of extractPdfLinks(html)) {
      if (seen.has(pdfUrl)) continue;
      seen.add(pdfUrl);
      const localFile = findLocalFile(title, files);
      docs.push({
        id: slugify(title),
        title,
        category: page.category,
        section: page.section,
        webUrl,
        pdfUrl,
        localPath: localFile ? `/documents/issf/${localFile}` : undefined,
      });
    }
  }

  const missing = docs.filter((d) => !d.localPath);
  if (missing.length) {
    console.warn(`Missing local files (${missing.length}):`);
    missing.forEach((d) => console.warn(' -', d.title));
  }

  const categories = [
    { id: 'all', name: 'All Documents' },
    { id: 'rulebook', name: 'Rule Book 2026' },
    { id: 'rifle', name: 'Rifle' },
    { id: 'technical', name: 'Technical Rules' },
    { id: 'integrity', name: 'Integrity & Ethics' },
    { id: 'safeguarding', name: 'Safeguarding' },
    { id: 'disciplinary', name: 'Disciplinary' },
    { id: 'eligibility', name: 'Eligibility' },
    { id: 'doping', name: 'Anti-Doping' },
    { id: 'committees', name: 'Committees' },
    { id: 'ain', name: 'AIN Regulations' },
    { id: 'forms', name: 'Forms' },
    { id: 'guides', name: 'Guides & Interpretation' },
  ];

  for (const doc of docs) {
    if (/audit committee|nominations committee/i.test(doc.title)) doc.category = 'committees';
    if (/neutrality|russian and belarusian/i.test(doc.title)) doc.category = 'ain';
    if (/rifle equipment|rifle annex|training manual rifle|rifle for issf/i.test(doc.title)) {
      doc.category = 'rifle';
    }
  }

  const ts = `// Auto-generated by scripts/generate-issf-rules-data.mjs — ${new Date().toISOString().slice(0, 10)}
export interface IssfRuleDocument {
  id: string;
  title: string;
  description?: string;
  category: string;
  section: string;
  webUrl?: string;
  pdfUrl?: string;
  localPath?: string;
}

export const issfRuleCategories = ${JSON.stringify(categories, null, 2)} as const;

export const issfSourceUrl = 'https://www.issf-sports.org/rules';

export const issfRuleDocuments: IssfRuleDocument[] = ${JSON.stringify(docs, null, 2)};
`;

  fs.mkdirSync(path.join(root, 'src', 'data'), { recursive: true });
  fs.writeFileSync(path.join(root, 'src', 'data', 'issf-rules.ts'), ts);
  console.log(`Wrote ${docs.length} documents to src/data/issf-rules.ts`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
